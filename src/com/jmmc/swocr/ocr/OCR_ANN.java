	public OCR_ANN(String route) {
		// cargar la red ya entrenada dentro del proyecto dentro de el jar
		ClassLoader classLoader = getClass().getClassLoader();
		InputStream is = classLoader.getResourceAsStream(route);
		//"com/jmmc/swocr/ocr/red_2_96.0773803722834.ser"
		
		red = new RNSerializer().deserializeRed(is);

		// iniciar el image manager
		manejador = new PngManager();
	}

	public char clasificar(File f) {
		if (red == null) {
			return '?';
		}

		byte[] flat = manejador.cargarArregloFlatDeImagen(f.toURI());

		double[] resultado = red.clasificar(flat);
		byte[] binarizedArray = binarizeArray(resultado);

		return check(binarizedArray);
	}

	private char check(byte[] muestra) {
		char is = '?';
		for (int i = 0; i < patrones_salidas.length; i++) {
			boolean es = true;
			for (int j = 0; j < patrones_salidas[i].length; j++) {
				es = es && (muestra[j] == patrones_salidas[i][j]);
			}
			if (es) {
				is = (char) (i + 65);
				break;
			}
		}
		return is;
	}

	private byte[] binarizeArray(double[] arr) {
		byte[] bi = new byte[arr.length];

		for (int i = 0; i < bi.length; i++) {
			bi[i] = (arr[i] > 0.5) ? (byte) 1 : (byte) 0;
		}
		return bi;
	}

